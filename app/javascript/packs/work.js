$("body").delegate(".work-post-container", "mouseenter", function() {
  show_read_more(this, 'work')
})

$("body").delegate(".work-post-container", "mouseover", function() {
  show_read_more(this, 'work')
})

$("body").delegate(".work-post-container", "mouseleave", function() {
  hide_read_more(this, 'work')
})

$("body").delegate(".work-post-container", "mouseleave", function() {
  hide_read_more(this, 'work')
})